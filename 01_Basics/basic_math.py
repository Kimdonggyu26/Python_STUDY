# 기본 산술 연산

a = 10
b = 3

print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")     # 실수 나눗셈
print(f"{a} // {b} = {a // b}")   # 정수 나눗셈
print(f"{a} % {b} = {a % b}")     # 나머지
print(f"{a} ** {b} = {a ** b}")   # 거듭제곱

# 실행결과
#   10 + 3 = 13
#   10 - 3 = 7
#   10 * 3 = 30
#   10 / 3 = 3.3333333333333335
#   10 // 3 = 3
#   10 % 3 = 1
#   10 ** 3 = 1000